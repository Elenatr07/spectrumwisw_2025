
$(".icon_be_svg").on('mouseover', function () {
    var icon = document.querySelector('.svg_hover_be');
    $(icon).css('display', 'block');
 }).on('mouseout', function () {
    var icon = document.querySelector('.svg_hover_be');
    $(icon).css('display', 'none');
 });

